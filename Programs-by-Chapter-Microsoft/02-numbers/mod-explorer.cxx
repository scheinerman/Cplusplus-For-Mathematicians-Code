#include <iostream>
using namespace std;

/**
 * A program to investigate the behavior of the mod (%) operation.
 */

int main() {
  int a,b;

  cout << "Enter the first number --> ";
  cin >> a;

  cout << "Enter the second number --> ";
  cin >> b;

  cout << a << " % " << b << " = " << a%b << endl;

  return 0;
}
